num1 = int(input("Digite o primeiro numero: "))
num2 = int(input("Digite o segundo numero: "))

def soma():
    return num1 + num2

def sub():
    return num1 - num2

def mult():
    return num1 * num2

def div():
    return num1 / num2

print("Soma: ", soma())
print("Subtração: ", sub())
print("Multplicação: ", mult())
print("Divisão: ", div())